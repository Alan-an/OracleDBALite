-- USER SQL
CREATE USER sch_spl IDENTIFIED BY schsp1schsp1
DEFAULT TABLESPACE "USERS"
TEMPORARY TABLESPACE "TEMP";

-- QUOTAS
ALTER USER sch_spl QUOTA UNLIMITED ON USERS;

-- ROLES

-- SYSTEM PRIVILEGES
GRANT CREATE TABLE TO sch_spl ;
GRANT CREATE JOB TO sch_spl ;
GRANT CREATE PROCEDURE TO sch_spl ;
GRANT CREATE SESSION TO sch_spl ;
GRANT SELECT ANY DICTIONARY TO sch_spl ;
GRANT ALTER SESSION TO sch_spl ;
GRANT READ, WRITE on DIRECTORY DIR_SPLUNK to SCH_SPL;
GRANT EXECUTE on UTL_FILE to SCH_SPL;
GRANT EXECUTE on DBMS_LOCK to SCH_SPL;

